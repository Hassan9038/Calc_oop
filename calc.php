<?php

class calc{

    // Method 
    // Add function 
    public function add($n1 , $n2){
        return ($n1 + $n2) ;
    }
    
    // Sub function
    public function sub($n1 , $n2){
        return ($n1 - $n2) ;
    }
    
    // mul function
    public function mul($n1 , $n2){
        return ($n1 * $n2) ;
    }
    
    // div function
    public function div($n1 , $n2){
        return ($n1 / $n2) ;
    }
}

